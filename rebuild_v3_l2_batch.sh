#!/bin/bash
# ============================================================
# V3 L2 缓存批量重建脚本（后台运行版）
# 重建缺失月份：2018-01 ~ 2024-12（共84个月）
# ============================================================

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
LOG="/tmp/rebuild_v3_l2.log"
PIDFILE="/tmp/rebuild_v3_l2.pid"

# 年-月列表（按顺序）
MONTHS=(
  "2018-01" "2018-02" "2018-03" "2018-04" "2018-05" "2018-06"
  "2018-07" "2018-08" "2018-09" "2018-10" "2018-11" "2018-12"
  "2019-01" "2019-02" "2019-03" "2019-04" "2019-05" "2019-06"
  "2019-07" "2019-08" "2019-09" "2019-10" "2019-11" "2019-12"
  "2020-01" "2020-02" "2020-03" "2020-04" "2020-05" "2020-06"
  "2020-07" "2020-08" "2020-09" "2020-10" "2020-11" "2020-12"
  "2021-01" "2021-02" "2021-03" "2021-04" "2021-05" "2021-06"
  "2021-07" "2021-08" "2021-09" "2021-10" "2021-11" "2021-12"
  "2022-01" "2022-02" "2022-03" "2022-04" "2022-05" "2022-06"
  "2022-07" "2022-08" "2022-09" "2022-10" "2022-11" "2022-12"
  "2023-01" "2023-02" "2023-03" "2023-04" "2023-05" "2023-06"
  "2023-07" "2023-08" "2023-09" "2023-10" "2023-11" "2023-12"
  "2024-01" "2024-02" "2024-03" "2024-04" "2024-05" "2024-06"
  "2024-07" "2024-08" "2024-09" "2024-10" "2024-11" "2024-12"
)

TOTAL=${#MONTHS[@]}
echo "=========================================="
echo "V3 L2 缓存批量重建（后台）"
echo "开始时间: $(date '+%Y-%m-%d %H:%M:%S')"
echo "总月份数: $TOTAL"
echo "日志文件: $LOG"
echo "=========================================="

start_time=$(date +%s)

for i in "${!MONTHS[@]}"; do
    MON="${MONTHS[$i]}"
    idx=$((i + 1))
    
    echo ""
    echo ">>> [$idx/$TOTAL] 重建 $MON"
    echo "开始: $(date '+%Y-%m-%d %H:%M:%S')"
    
    /usr/bin/python3 $SCRIPT_DIR/wavechan_daily_incremental.py \
        --rebuild-month "$MON" \
        >> $LOG 2>&1
    
    exit_code=$?
    
    end_time=$(date +%s)
    elapsed=$((end_time - start_time))
    echo "<<< [$idx/$TOTAL] $MON 完成 (用时: ${elapsed}秒, 退出码: $exit_code)"
    echo "=== $(date '+%Y-%m-%d %H:%M:%S') ===" >> $LOG
    
    if [ $exit_code -ne 0 ]; then
        echo "!!! 警告: $MON 重建失败，退出码 $exit_code，继续下一个"
    fi
done

echo ""
echo "=========================================="
echo "全部完成！"
echo "结束时间: $(date '+%Y-%m-%d %H:%M:%S')"
echo "总耗时: $(( ($(date +%s) - start_time) / 60 )) 分钟"
echo "=========================================="
